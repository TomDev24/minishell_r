#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char *res;

	res = 0;
	while (*s != (char)c && *s != '\0')
		s++;
	if ((char)c == '\0' || *s == (char)c)
		res = (char*)s;
	return (res);
}
